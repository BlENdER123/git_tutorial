# чтобы подключить модуль нужно прописать команду import module_name
# после этого будет доступны все функции этого модулся через точку module_name.name_func()

# чтобы подключить только отдельные функции, переменные  нужно прописить from module_name import name_func, name_var
# после этого будет доступна эта функция name_func()

# чтобы подключить функцию и переименовать ее у себя в файле нужно прописать from module_name import name_func as your_name_func